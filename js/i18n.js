const I18n = {
    currentLang: 'en',
    _elementsCache: null,

    setLanguage: (lang) => {
        if (translations[lang]) {
            I18n.currentLang = lang;
            I18n.updateDOM();
        }
    },

    updateDOM: () => {
        if (!I18n._elementsCache) {
            I18n._elementsCache = document.querySelectorAll('[data-i18n]');
        }

        I18n._elementsCache.forEach(el => {
            const key = el.dataset.i18n;
            if (translations[I18n.currentLang][key]) {
                el.textContent = translations[I18n.currentLang][key];
            }
        });
    },

    getAdvCues: () => {
        return ADV_CUES[I18n.currentLang] || ADV_CUES.en;
    },

    getAdvPurposes: () => {
        return ADV_PURPOSES[I18n.currentLang] || ADV_PURPOSES.en;
    }
};

export default I18n;
